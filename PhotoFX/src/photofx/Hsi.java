
package photofx;

public class Hsi {
    private int h;
    private int s;
    private int i;
    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }
    
    public Hsi(int h, int s, int i) {
        this.h = h;
        this.s = s;
        this.i = i;
    }
    
}
